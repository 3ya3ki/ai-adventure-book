  // === Preload all rounds in background (parallel) ===
  async function preloadAllRounds() {
    const gen = _generation; // snapshot: discard results if a new game started
    await Promise.all(
      Array.from({ length: _state.totalRounds }, (_, i) =>
        loadRoundData(i).then(data => {
          // Only write if still the same game session
          if (_generation === gen) {
            _preloadedData[i] = data;
            // Record theme to sessionStorage for cross-session deduplication
            if (data?.theme) {
              try {
                const used = JSON.parse(sessionStorage.getItem('trial-used-themes') || '[]');
                if (!used.includes(data.theme)) {
                  used.push(data.theme);
                  if (used.length > 20) used.shift(); // cap at 20 entries
                  sessionStorage.setItem('trial-used-themes', JSON.stringify(used));
                }
              } catch (e) {}
            }
          }
        })
      )
    );
  }

  // === Round data loading ===
  async function loadRoundData(roundIndex) {
    if (_preloadedData[roundIndex]) return _preloadedData[roundIndex];
    try {
      const result = await ErrorHandler.retryWithBackoff(async () => {
        const r = await fetch('/api/trial', {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({
            action: 'generate_round',
            theme_hint: _roundCategoryHints[roundIndex] || null,
            excluded_themes: (() => { try { return JSON.parse(sessionStorage.getItem('trial-used-themes') || '[]'); } catch (e) { return []; } })(),
          }),
        });
        if (!r.ok) throw new Error(`HTTP ${r.status}`);
        const data = await r.json();
        if (!data.turns || !data.turns.length) throw new Error('Invalid response');
        return data;
      }, 1);
      return result;
    } catch (e) {
      console.warn('[Trial] API unavailable, using fallback:', e.message);
      return MOCK_ROUNDS[_selectedIndices[roundIndex]];
    }
  }

