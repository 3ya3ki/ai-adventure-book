      _gameRunning = false;
      _advanceTapEnabled = false;
      _typewriting = false;
      _preloadedData = [];
      // Unblock any pending showDefenseUI / showReveal so their Promises settle
      if (_cancelCurrentInteraction) {
        _cancelCurrentInteraction();
        _cancelCurrentInteraction = null;
      }
