  // === Single round flow ===
  async function runRound(roundIndex) {
    if (!_gameRunning) return;

    const badge = document.getElementById('trial-round-badge');
    if (badge) badge.textContent = `Round ${roundIndex + 1} / ${_state.totalRounds}`;

    if (_chatEl) _chatEl.innerHTML = '';
    _state.roundLayers = [];

    const alreadyLoaded = !!_preloadedData[roundIndex];
    if (!alreadyLoaded) showLoading();
    const roundData = await loadRoundData(roundIndex);
    if (!alreadyLoaded) hideLoading();

    if (!_gameRunning) return;

    await skippableDelay(PRE_CUTIN_DELAY);
    await showCutin('開廷！', 'COURT IN SESSION', CHARS.judge.color);
    if (roundIndex === 0) {
      await addMessage('judge', `これより、${roundData.theme}事件の公判を開廷する。……罪状を読み上げる。（……なお、裁判長は先入観を持っていない。）`);
    } else {
      await addMessage('judge', `開廷する。${roundData.theme}事件。……罪状を読み上げる。`);
    }
    await addMessage('judge', roundData.indictment);
    if (roundIndex === 0) {
      await addMessage('judge', '検察側、冒頭陳述を。（……手短に頼む。）');
    } else {
      await addMessage('judge', '検察側、冒頭陳述を行え。（……頼むから、まともな話であってくれ。）');
    }
    await addMessage('prosecution', roundData.prosecution_opening);
    await addMessage('judge', '証人、自らの主張を述べよ。（……裁判長は公正に聞く。公正に。）');
    await addMessage('defendant', roundData.defendant_initial);
    _state.roundLayers.push(summarize(roundData.defendant_initial));
    _state.totalLayers++;

    for (let t = 0; t < roundData.turns.length; t++) {
      if (!_gameRunning) return;
      const turnData = roundData.turns[t];

      await skippableDelay(PRE_CUTIN_DELAY);
      await showCutin('異議あり！', 'OBJECTION!', CHARS.prosecution.color);
      await addMessage('prosecution', turnData.prosecution);

      const result = await showDefenseUI(turnData).catch(e => {
        if (e.name === 'AbortError') return null;
        throw e;
      });
      if (result === null || !_gameRunning) return;

      await skippableDelay(PRE_CUTIN_DELAY);
      await showCutin('証拠を見ろ！', 'TAKE  THAT!', CHARS.defense.color);
      await addMessage('defense', `証拠を提示します——「${result.choice.label}」。${result.freeText ? result.freeText : ''}`);
      _state.totalDefenseChars += result.freeText.length;
      _state.totalLayers++;

      await addCorneredMessage(t);
      const tier = JUDGE_CONTINUE[Math.min(t, JUDGE_CONTINUE.length - 1)];
      await addMessage('judge', tier[Math.floor(Math.random() * tier.length)]);
      await addMessage('defendant', result.choice.testimony);
      _state.roundLayers.push(summarize(result.choice.testimony));
    }

    if (!_gameRunning) return;

    await skippableDelay(PRE_CUTIN_DELAY);
    await showCutin('閉廷！', 'COURT ADJOURNED', CHARS.judge.color);
    await addMessage('judge', 'これにて閉廷とする。（……一つだけ聞いていいか。……いや、やめておく。）閉廷。');

    const revealAborted = await showReveal(roundData, roundIndex).then(() => false).catch(e => {
      if (e.name === 'AbortError') return true;
      throw e;
    });
    if (revealAborted || !_gameRunning) return;
    _state.completedRounds++;
  }

  function summarize(text) {
    return text.length > 60 ? text.substring(0, 58) + '…' : text;
  }

  // === Rank computation ===
  // S: 2ラウンド完走 + 弁護文字数30字以上（熱心な弁護士）
  // A: 2ラウンド完走
  // B: 1ラウンド完走
  // C: 0ラウンド（途中終了）
  function computeRank() {
    const rounds = _state.completedRounds;
    const chars  = _state.totalDefenseChars;
    if (rounds >= 2 && chars >= 30) return { rank: 'S', title: 'ハルシネーション・マスター' };
    if (rounds >= 2)                 return { rank: 'A', title: '嘘の弁護士' };
    if (rounds >= 1)                 return { rank: 'B', title: '駆け出し弁護人' };
    return                                  { rank: 'C', title: '正直者' };
  }

