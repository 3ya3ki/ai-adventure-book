  // === Exhibition timer ===
  function startTimer(seconds) {
    let remaining = seconds;
    const el = document.getElementById('trial-timer');
    if (!el) return;

    function update() {
      const m = Math.floor(remaining / 60);
      const s = remaining % 60;
      el.textContent = `${m}:${String(s).padStart(2, '0')}`;
      if (remaining <= 60) el.classList.add('trial-timer--warning');
    }

    update();
    _timerIntervalId = setInterval(() => {
      remaining--;
      update();
      if (remaining <= 0) {
        clearInterval(_timerIntervalId);
        _timerIntervalId = null;
        _gameRunning = false;
        // Capture generation: if destroy()+init() fires within 800ms, showForcedEnd is skipped
        const gen = _generation;
        setTimeout(() => { if (_generation === gen) showForcedEnd(); }, 800);
      }
    }, 1000);
  }

  function stopTimer() {
    if (_timerIntervalId) {
      clearInterval(_timerIntervalId);
      _timerIntervalId = null;
    }
  }

